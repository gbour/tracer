-module(tracer_app).

-behaviour(application).

%% Application callbacks
-export([start/0, start/2, stop/1]).

%%====================================================================
%% API
%%====================================================================

start() ->
    application:ensure_all_started(tracer),
    ok.

start(_StartType, _StartArgs) ->
    tracer_sup:start_link().

%%--------------------------------------------------------------------
stop(_State) ->
    ok.

